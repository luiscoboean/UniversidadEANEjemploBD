    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = cNombre.getText().trim().toUpperCase();
        String carrera = cCarrera.getText().trim().toUpperCase();
        int semestre = 0;
        try {
            semestre = Integer.parseInt(cSemestre.getText().trim());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El semestre debe ser numerico!");
            return ;
        }
        double prom = 0.0;
        try {
            prom = Double.parseDouble(cPromedio.getText().trim());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El promedio debe ser num√©rico!");
            return ;
        }
        
        if (Universidad.insertarEstudianteEnLaBD(nombre, carrera, semestre, prom)) {
            int id = Universidad.obtenerIDDelUltimoEstudiante();
            JOptionPane.showMessageDialog(null, "Estudiante guardado con ID = " + id);
            
            cNombre.setText("");
            cCarrera.setText("");
            cSemestre.setText("");
            cPromedio.setText("");
            
            cNombre.requestFocus();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


