    private void mostrarEstudiantes() {
        ArrayList<String[]> estuds = Universidad.obtenerTodosLosEstudiantes();
        
        // Creamos una matriz con los estudiantes y la columnas
        String modelo[][] = new String[estuds.size()][5];
        
        for (int i = 0; i < estuds.size(); ++i) {
            for (int j = 0; j < 5; j++) {
                modelo[i][j] = estuds.get(i)[j];
            }
        }
        
        DefaultTableModel dataModel = new DefaultTableModel(
                modelo, 
                new String [] {
                "ID", "Nombres", "Carrera", "Semestre", "Promedio"
            });
        tablaEstudiantes.setModel(dataModel);
    }
